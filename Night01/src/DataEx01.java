public class DataEx01 {
	public static void main(String[] args) {
		System.out.println(100L);//long 정수타입(자료형) 값 뒤에는 접미사 L(l)이 붙는다.
        System.out.println(10.5f);//float 실수 숫자 타입 값 뒤에는 접미사 F 또는 f가 붙는다.
        System.out.println('a');//char 단일문자 'a'는 십진수 정수로 저장됨
        System.out.println(false);//거짓을 뜻하는 boolean 논리 타입 => 조건판단
        System.out.println("홍길동");//"홍길동"은 문자열 참조타입
        //System.out은 모니터 출력장치와 연결됨. print()는 출력하라는 뜻, ln은 줄바꿈
	}
}

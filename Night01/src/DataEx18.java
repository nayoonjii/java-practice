public class DataEx18 {
	public static void main(String[] args) {

		int a=12;
		int b=20;
		int result;
		
		result=a&b;
		System.out.println(a+" & "+b+" : "+result);
		
		result=a|b;
		System.out.println(a+" | "+b+" : "+result);
		
		result=a^b;
		System.out.println(a+" ^ "+b+" : "+result);
		
		result=~a;
		System.out.println("~"+a+" : "+ result);
	}
}

public class DataEx07 {
	public static void main(String[] args) {

		System.out.println("\'");// \' 은 작은따옴표로 표시(확장 특수문자)
		System.out.println("홍길동\t이순신");// \t는 탭키만큼 수평으로 띄움
		System.out.println("\n");//\은 줄바꿈
		System.out.println("\"방갑습니다\"");//\"는 큰따옴표로 표시
		System.out.println("c:\\"); // \\은 \로 표시, 경로구분할 때 많이 사용
	}
}
